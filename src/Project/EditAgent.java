private boolean addAgent(String AgentId, String First_Name, String Last_Name,
            String Agent_Contact_No) {
        
       String insert_query = "Insert into agent values(?,?,?,?)";
        
        try {
            
            PreparedStatement stmt = Database.connection.prepareStatement(insert_query);
            
            stmt.setString(1, AgentId);    stmt.setString(2, First_Name);    stmt.setString(3, Last_Name);
            stmt.setString(4, Agent_Contact_No);
            
            System.out.println("row affected: " + stmt.executeUpdate());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Unsuccesfull", JOptionPane.ERROR_MESSAGE);
            return false;
        }
       
        return true;
    }

    
    private void updateAgent(String AgentId, String First_Name, String Last_Name,
            String Agent_Contact_No) {
        String update_query = "Update agent set First_Name = ? , Last_Name = ?, Agent_Contact_No = ? where Agent_Id = ?";
        int rowsAffected = 0;
        try {
            
            PreparedStatement stmt = Database.connection.prepareStatement(update_query);
            
            stmt.setString(1, First_Name);    stmt.setString(2, Last_Name);
            stmt.setString(3, Agent_Contact_No);    stmt.setString(4, AgentId);
            rowsAffected = stmt.executeUpdate();
            System.out.println("row affected: " + rowsAffected);
            JOptionPane.showMessageDialog(this, "rows: affected: " + rowsAffected , "succesfull", JOptionPane.OK_OPTION);
            updateAgentId.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Unsuccesfull", JOptionPane.ERROR_MESSAGE);
        }
        
       
    }

