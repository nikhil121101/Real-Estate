    private void proceedUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedUpdateActionPerformed
        if(updateAgentId.getText().isBlank() || updateAgentId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "EstateId can't be empty!",
                "Unsuccesfull", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int text_size = 30;

        JTextField First_Name = new JTextField(text_size);
        JTextField Last_Name = new JTextField(text_size);
        JTextField Agent_ContactNo = new JTextField(text_size);

        JPanel agentPannel = new JPanel(new GridLayout(0,1));

        agentPannel.add(new JLabel("First Name:"));
        agentPannel.add(First_Name);

        agentPannel.add(new JLabel("Last Name:"));
        agentPannel.add(Last_Name);

        agentPannel.add(new JLabel("contact NO:"));
        agentPannel.add(Agent_ContactNo);

        int result = JOptionPane.showConfirmDialog(null, agentPannel,
            "Please Enter New Updated Estate details", JOptionPane.OK_CANCEL_OPTION);
        if (result == JOptionPane.OK_OPTION) {
            if(!(
                First_Name.getText().isBlank()  || First_Name.getText().isEmpty() ||
                Last_Name.getText().isBlank()  || Last_Name.getText().isEmpty() ||
                Agent_ContactNo.getText().isBlank()  || Agent_ContactNo.getText().isEmpty()
            )) {

                updateAgent(
                    updateAgentId.getText(),
                    First_Name.getText(),
                    Last_Name.getText(),
                    Agent_ContactNo.getText()
                );

            }

            else {

                JOptionPane.showMessageDialog(this, "All attributes are mandatory!",
                    "Unsuccesfull", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_proceedUpdateActionPerformed

